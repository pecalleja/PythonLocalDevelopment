import pytest

from src.codesignal.dynamic.coin_change import solution as coin_change


@pytest.mark.parametrize(
    "coins, amount, expected",
    [
        ([1, 2, 5], 11, 11),
        ([2], 3, 0),
        ([1], 0, 1),
        ([7, 2, 3], 28, 14),
        ([5, 10], 20, 3),
        ([], 0, 1),
        ([1, 4, 3], 50, 121),
        ([9, 6, 5, 4, 2], 73, 1024),
        ([98, 70, 15], 175, 1),
        ([1, 2, 3], 4, 4),
        ([10, 25, 50, 100], 1000, 946),
        ([1, 2], 5000, 2501),
        ([19, 23, 58, 73], 97, 0),
        ([53, 250, 21, 45], 453, 4),
        ([199, 50, 78, 12], 987, 21),
        ([275, 198, 435, 11], 895, 0),
        ([81, 67, 29, 14], 4300, 6422),
        ([512, 272, 344, 62], 4510, 16),
        ([725, 410, 982, 630], 3685, 0),
        ([500, 1000, 2000, 5000], 5000, 13),
    ],
)
def test_coin_change(coins, amount, expected):
    assert coin_change(coins, amount) == expected
